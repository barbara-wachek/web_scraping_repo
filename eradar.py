#%% KROK 2: Zbierz linki ze wszystkich kategorii
print("\n=== Pobieram linki z kategorii ===")
articles_links = []

for cat_slug in CATEGORIES:
    cat_name = cat_slug.replace('%2C', ',')
    links_p1, total_pages = get_category_links(cat_slug, 1)
    print(f"{cat_name}: {len(links_p1)} artykułów, {total_pages} stron")
    articles_links.extend(links_p1)

    for page_num in range(2, total_pages + 1):
        links_pn, _ = get_category_links(cat_slug, page_num)
        articles_links.extend(links_pn)
