def get_listing_links(page_num):
    """Pobiera linki do postów z jednej strony listingu"""
    if page_num == 1:
        url = f"{BASE_URL}/category/ksiega-przyjaciol/"
    else:
        url = f"{BASE_URL}/category/ksiega-przyjaciol/page/{page_num}/"
    try:
        r = requests.get(url, headers=HEADERS, timeout=30)
        r.encoding = 'utf-8'
        if r.status_code != 200:
            return []
        soup = BeautifulSoup(r.text, 'lxml')
        links = []
        for a in soup.select('div.padding-cat-inner a.h2'):
            href = a.get('href', '')
            if href and href.startswith('http'):
                links.append(href)
        return links
    except Exception as e:
        print(f"  Błąd strona {page_num}: {e}")
        return []


def dictionary_of_article(article_link):
    """Scrapuje pojedynczy post"""
    try:
        r = requests.get(article_link, headers=HEADERS, timeout=30)
        r.encoding = 'utf-8'
        if r.status_code != 200:
            print(f"  [{r.status_code}] {article_link}")
            return None
        soup = BeautifulSoup(r.text, 'lxml')

        # Tytuł
        try:
            h1 = soup.select_one('section#single-post h1')
            title_of_article = h1.text.strip() if h1 else ""
        except:
            title_of_article = ""

        # Data - "20 04 2026"
        date_of_publication = ""
        try:
            date_span = soup.select_one('span.post-date')
            if date_span:
                raw = date_span.text.strip()  # "20 04 2026"
                parts = raw.split()
                if len(parts) == 3:
                    date_of_publication = f"{parts[2]}-{parts[1]}-{parts[0]}"
        except:
            pass

        # Autor
        author = ""
        try:
            author_div = soup.select_one('section#single-post div.h4')
            if author_div:
                author = author_div.text.strip()
        except:
            pass

        # Tekst artykułu
        article = ""
        try:
            text_div = soup.select_one('section#single-post div.mb-4')
            if text_div:
                article = text_div.get_text(separator='\n', strip=True)
                article = re.sub(r'\n{3,}', '\n\n', article).replace('\xa0', ' ').strip()
        except:
            pass

        # Zdjęcia
        photos_links = None
        try:
            text_div = soup.select_one('section#single-post div.mb-4')
            if text_div:
                imgs = text_div.find_all('img')
                photo_urls = [img['src'] for img in imgs if img.get('src') and 'wp-content/uploads' in img['src']]
                photos_links = ' | '.join(photo_urls) if photo_urls else None
        except:
            pass

        # Zdjęcie profilowe autora
        author_photo = None
        try:
            author_img = soup.select_one('section#single-post div.cat-group img.rounded-circle')
            if author_img and author_img.get('src'):
                author_photo = author_img['src']
        except:
            pass

        return {
            "Link": article_link,
            "Data publikacji": date_of_publication,
            "Tytuł artykułu": title_of_article,
            "Tekst artykułu": article,
            "Autor": author,
            "Tagi": "Księga Przyjaciół",
            "Linki zewnętrzne": None,
            "Zdjęcia/Grafika": True if photos_links else False,
            "Linki do zdjęć": photos_links
        }
    except Exception as e:
        print(f"  WYJĄTEK: {article_link} - {e}")
        return None

