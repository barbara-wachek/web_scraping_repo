print(f"\n=== Pobieram linki ===")
if TEST_MODE:
    articles_links = get_listing_links(1)[:10]
else:
    articles_links = []
    for page_num in tqdm(range(1, TOTAL_PAGES + 1), desc="Strony"):
        links = get_listing_links(page_num)
        articles_links.extend(links)
        time.sleep(0.3)
    articles_links = list(dict.fromkeys(articles_links))

print(f"Linki: {len(articles_links)}")
for link in articles_links[:5]:
    print(f"  {link}")

#%% KROK 3: Scrapuj
print(f"\n=== Scrapuję {len(articles_links)} artykułów ===")
all_results = []
errors = []

if TEST_MODE:
    for i, link in enumerate(articles_links):
        print(f"[{i+1}/{len(articles_links)}] {link}")
        result = dictionary_of_article(link)
        if result:
            all_results.append(result)
            print(f"  ✅ '{result['Tytuł artykułu'][:50]}' | {result['Data publikacji']} | {result['Autor']}")
        else:
            errors.append(link)
            print(f"  ❌")
        time.sleep(0.5)
else:
    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
        futures = {executor.submit(dictionary_of_article, link): link for link in articles_links}
        for future in tqdm(as_completed(futures), total=len(futures), desc="Artykuły"):
            result = future.result()
            if result is not None:
                all_results.append(result)
            else:
                errors.append(futures[future])

print(f"\n=== WYNIK: {len(all_results)}/{len(articles_links)} ===")

#%% KROK 4: Podgląd
